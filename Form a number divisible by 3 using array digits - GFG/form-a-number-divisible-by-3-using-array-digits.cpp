class Solution {
  public:
    int isPossible(int N, int arr[]) {
        string s="";
        for(int i=0;i<N;i++) s+=to_string(arr[i]);
        
        long long int ans=0;
        
        for(auto it:s) ans+=it - '0';
        
        if(ans % 3==0) return 1;
        return 0;
    }
};

